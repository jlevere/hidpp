fn bitmap_to_icon(bitmap: &[[u8; 18]; 18]) -> anyhow::Result<Icon> {
    let mut rgba = vec![0u8; (W * H * 4) as usize];
    for (y, row) in bitmap.iter().enumerate() {
        for (x, &pixel) in row.iter().enumerate() {
            if pixel == 1 {
                let off = (y * W as usize + x) * 4;
                rgba[off] = 0; // R — black, macOS template tinting handles the rest
                rgba[off + 1] = 0; // G
                rgba[off + 2] = 0; // B
                rgba[off + 3] = 255; // A
            }
        }
    }
    Icon::from_rgba(rgba, W, H).map_err(|e| anyhow::anyhow!("icon: {e}"))
}

pub fn connected() -> anyhow::Result<Icon> {
    bitmap_to_icon(&SOLID)
}

pub fn disconnected() -> anyhow::Result<Icon> {
    bitmap_to_icon(&OUTLINE)
}
